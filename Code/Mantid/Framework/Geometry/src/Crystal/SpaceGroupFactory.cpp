/// Free function that tries to parse the given list of symmetry operations and returns true if successfull.
bool isValidGeneratorString(const std::string &generatorString)
{
    std::vector<std::string> generatorStrings;
    boost::split(generatorStrings, generatorString, boost::is_any_of(";"));

    for(auto it = generatorStrings.begin(); it != generatorStrings.end(); ++it) {
        try {
            SymmetryOperationSymbolParser::parseIdentifier(*it);
        } catch(Kernel::Exception::ParseError) {
            return false;
        }
    }

    return true;
}

/// Constructor for AbstractSpaceGroupGenerator
AbstractSpaceGroupGenerator::AbstractSpaceGroupGenerator(size_t number, const std::string &hmSymbol, const std::string &generatorInformation) :
    m_number(number),
    m_hmSymbol(hmSymbol),
    m_generatorString(generatorInformation),
    m_prototype()
{

}

/// Returns the internally stored prototype, generates one if necessary.
SpaceGroup_const_sptr AbstractSpaceGroupGenerator::getPrototype()
{
    if(!hasValidPrototype()) {
        m_prototype = generatePrototype();
    }

    return m_prototype;
}

/**
 * Generates a prototype space group object
 *
 * Constructs a space group object using a Group generated by the pure virtual
 * generateGroup()-method along with the stored number and Hermann-Mauguin-symbol.
 *
 * generateGroup() has to be implemented by sub-classes and should probably use
 * the generatorInformation supplied to the constructor.
 *
 * @return SpaceGroup prototype object
 */
SpaceGroup_const_sptr AbstractSpaceGroupGenerator::generatePrototype()
{
    Group_const_sptr generatingGroup = generateGroup();

    if(!generatingGroup) {
        throw std::runtime_error("Could not create group from supplied symmetry operations.");
    }

    return boost::make_shared<const SpaceGroup>(m_number, m_hmSymbol, *generatingGroup);
}

/// Constructor of AlgorithmicSpaceGroupGenerator which throws an std::runtime_error exception when the generatorInformation string cannot be interpreted as symmetry operations.
AlgorithmicSpaceGroupGenerator::AlgorithmicSpaceGroupGenerator(size_t number, const std::string &hmSymbol, const std::string &generatorInformation) :
    AbstractSpaceGroupGenerator(number, hmSymbol, generatorInformation)
{
    if(!isValidGeneratorString(generatorInformation)) {
        throw std::runtime_error("Generator string could not be parsed: " + generatorInformation);
    }
}

/// Uses an algorithm based on Shmueli, U. Acta Crystallogr. A 40, 559–567 (1984) to generate the group.
Group_const_sptr AlgorithmicSpaceGroupGenerator::generateGroup() const
{
    Group_const_sptr baseGroup = GroupFactory::create<ProductOfCyclicGroups>(getGeneratorString());
    Group_const_sptr centeringGroup = GroupFactory::create<CenteringGroup>(getCenteringSymbol());

    return baseGroup * centeringGroup;
}

/// Returns the centering symbol, which is extracted from the Hermann-Mauguin-symbol.
std::string AlgorithmicSpaceGroupGenerator::getCenteringSymbol() const
{
    return getHMSymbol().substr(0, 1);
}

/// Constructor of TabulatedSpaceGroupGenerator, throws an std::runtime_error exception if generatorInformation cannot be intepreted as symmetry operations.
TabulatedSpaceGroupGenerator::TabulatedSpaceGroupGenerator(size_t number, const std::string &hmSymbol, const std::string &generatorInformation) :
    AbstractSpaceGroupGenerator(number, hmSymbol, generatorInformation)
{
    if(!isValidGeneratorString(generatorInformation)) {
        throw std::runtime_error("Generator string could not be parsed: " + generatorInformation);
    }
}

/// Returns a group that contains the symmetry operations in
Group_const_sptr TabulatedSpaceGroupGenerator::generateGroup() const
{
    return GroupFactory::create<Group>(getGeneratorString());
}


/// Creates a space group given the Hermann-Mauguin symbol, throws std::invalid_argument if symbol is not registered.
SpaceGroup_const_sptr SpaceGroupFactoryImpl::createSpaceGroup(const std::string &hmSymbol)
{
    if(!isSubscribed(hmSymbol)) {
        throw std::invalid_argument("Space group with symbol '" + hmSymbol + "' is not registered.");
    }

    return constructFromPrototype(getPrototype(hmSymbol));
}

/// Returns true if space group with given symbol is subscribed.
bool SpaceGroupFactoryImpl::isSubscribed(const std::string &hmSymbol) const
{
    return m_generatorMap.find(hmSymbol) != m_generatorMap.end();
}

/// Returns true if space group with given number is subscribed.
bool SpaceGroupFactoryImpl::isSubscribed(size_t number) const
{
    return m_numberMap.find(number) != m_numberMap.end();
}

/// Returns a vector with all subscribed space group symbols.
std::vector<std::string> SpaceGroupFactoryImpl::subscribedSpaceGroupSymbols() const
{
    std::vector<std::string> symbols;
    symbols.reserve(m_generatorMap.size());

    for(auto it = m_generatorMap.begin(); it != m_generatorMap.end(); ++it) {
        symbols.push_back(it->first);
    }

    return symbols;
}

/// Returns a vector with all symbols that correspond to a space group number
std::vector<std::string> SpaceGroupFactoryImpl::subscribedSpaceGroupSymbols(size_t number) const
{
    std::vector<std::string> symbols;

    auto keyPair = m_numberMap.equal_range(number);

    for(auto it = keyPair.first; it != keyPair.second; ++it) {
        symbols.push_back(it->second);
    }

    return symbols;
}

/// Returns a vector with all subscribed space group numbers.
std::vector<size_t> SpaceGroupFactoryImpl::subscribedSpaceGroupNumbers() const
{
    std::vector<size_t> numbers;
    numbers.reserve(m_numberMap.size());

    for(auto it = m_numberMap.begin(); it != m_numberMap.end(); it = m_numberMap.upper_bound(it->first)) {
        numbers.push_back(it->first);
    }

    return numbers;
}

/// Unsubscribes the space group with the given Hermann-Mauguin symbol, but throws std::invalid_argument if symbol is not registered.
void SpaceGroupFactoryImpl::unsubscribeSpaceGroup(const std::string &hmSymbol)
{
    if(!isSubscribed(hmSymbol)) {
        throw std::invalid_argument("Cannot unsubscribe space group that is not registered.");
    }

    auto eraseGenerator = m_generatorMap.find(hmSymbol);
    AbstractSpaceGroupGenerator_sptr generator = eraseGenerator->second;

    auto eraseNumber = m_numberMap.find(generator->getNumber());

    m_numberMap.erase(eraseNumber);
    m_generatorMap.erase(eraseGenerator);
}

/**
 * Subscribes a space group into the factory using generators
 *
 * With this method one can register a space group that is generated by an algorithm
 * based on the instructions in [1]. Currently it's important that the Herrman-
 * Mauguin symbol starts with an upper case letter, because that is used to generate
 * centering translations (it should be upper case anyway).
 *
 * The method will throw an exception if the number or symbol is already registered.
 *
 * [1] Shmueli, U. Acta Crystallogr. A 40, 559–567 (1984).
 *     http://dx.doi.org/10.1107/S0108767384001161
 *
 * @param number :: Space group number according to International Tables for Crystallography A
 * @param hmSymbol :: Herrman-Mauguin symbol with upper case first letter (centering).
 * @param generators :: Generating symmetry operations.
 */
void SpaceGroupFactoryImpl::subscribeGeneratedSpaceGroup(size_t number, const std::string &hmSymbol, const std::string &generators)
{
    subscribeUsingGenerator<AlgorithmicSpaceGroupGenerator>(number, hmSymbol, generators);
}

/// Subscribes a "tabulated space group" into the factory where all symmetry operations need to be supplied, including centering.
void SpaceGroupFactoryImpl::subscribeTabulatedSpaceGroup(size_t number, const std::string &hmSymbol, const std::string &symmetryOperations)
{
    subscribeUsingGenerator<TabulatedSpaceGroupGenerator>(number, hmSymbol, symmetryOperations);
}

/// Returns a copy-constructed instance of the supplied space group prototype object.
SpaceGroup_const_sptr SpaceGroupFactoryImpl::constructFromPrototype(const SpaceGroup_const_sptr prototype) const
{
    return boost::make_shared<const SpaceGroup>(*prototype);
}

/// Returns a prototype object for the requested space group.
SpaceGroup_const_sptr SpaceGroupFactoryImpl::getPrototype(const std::string &hmSymbol)
{
    AbstractSpaceGroupGenerator_sptr generator = m_generatorMap.find(hmSymbol)->second;

    if(!generator) {
        throw std::runtime_error("No generator for symbol '" + hmSymbol + "'");
    }

    return generator->getPrototype();
}

/// Puts the space group factory into the factory.
void SpaceGroupFactoryImpl::subscribe(const AbstractSpaceGroupGenerator_sptr &generator)
{
    if(!generator) {
        throw std::runtime_error("Cannot register null-generator.");
    }

    m_numberMap.insert(std::make_pair(generator->getNumber(), generator->getHMSymbol()));
    m_generatorMap.insert(std::make_pair(generator->getHMSymbol(), generator));
}


/// Constructor cannot be called, since SingletonHolder is used.
SpaceGroupFactoryImpl::SpaceGroupFactoryImpl() :
    m_numberMap(),
    m_generatorMap()
{
    Kernel::LibraryManager::Instance();
}

